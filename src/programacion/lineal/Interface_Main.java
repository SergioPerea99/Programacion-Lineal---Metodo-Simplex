    public void setVisibilidad_FuncionObjetivo(boolean b){
        String cadena = "Z = ";
        for (int i = 0; i < funcion_objetivo.getMultiplosVar().size(); i++){
            cadena += " ("+funcion_objetivo.getMultiplosVar().get(i)+")x"+(i+1);
            if (i < funcion_objetivo.getMultiplosVar().size() -1)
                cadena += " +";
        }
        jLabel3.setText(cadena);

        jLabel3.setVisible(b);
        
    }
    
    public ArrayList get_Var_Decision(){
        return funcion_objetivo.getMultiplosVar();
    }
    
    public void mostrarSolucion(){
        String solucion = "La solución óptima es Z = "+ restricciones.getTable().getValueAt(2, restricciones.getTable().getRowCount()-1)+". \n\n";
        for (int i = 0; i < NUM_VAR_DECISION; i++) {
            solucion += restricciones.getTable().getValueAt(i, 1).toString()+" = "+restricciones.getTable().getValueAt(i, 2)+". \n\n";
        }
        jTextArea1.setText(solucion);
        jTextArea1.setVisible(true);
    }
    
    public void mostrarParametros(){
        String cadena = "Z = ";
        for (int i = 0; i < funcion_objetivo.getMultiplosVar().size(); i++) {
            cadena += "("+funcion_objetivo.getMultiplosVar().get(i)+")x"+(i+1);
            if (i <funcion_objetivo.getMultiplosVar().size() -1)
                cadena += " + ";
        }
        cadena +="\n\n sujeto a\n\n";
        for (int i = 0; i < NUM_RESTRICCIONES; i++)
            cadena += restricciones.obtenerRestricciones()[i]+".\n\n";
        
        jTextArea2.setText(cadena);
        jTextArea2.setVisible(true);
    }

